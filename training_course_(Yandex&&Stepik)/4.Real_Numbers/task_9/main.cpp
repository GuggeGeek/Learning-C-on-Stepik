#include <iostream>
#include <cmath>

int main() {
    double a, b, c;
    std::cin >> a >> b >> c;

    double Dis = b * b - 4 * a * c;

    if (Dis < 0) return 0;

    if (Dis == 0) {
        std::cout << -b / (2 * a);
        return 0;
    }

    double sqrtDis = std::sqrt(Dis);
    double x1 = (-b - sqrtDis) / (2 * a);
    double x2 = (-b + sqrtDis) / (2 * a);

    std::cout << x1 << " " << x2;
    return 0;
}